package com.ibusiness.bpm.graph;

/**
 * 流程定义图形元素类
 * 
 * @author JiangBo
 * 
 */
public class GraphElement {
    private String id;
    private String name;
    
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
